  <h1>Politique de Confidentialité</h1>
  <p class="updated">Dernière mise à jour : 11 juin 2026</p>

  <h2>1. Préambule</h2>
  <p>Le site chat-caramail.fr (ci-après « le Site ») fournit une interface technique permettant d'accéder à un service de discussion en ligne exploité par un prestataire tiers indépendant. Le Site édite uniquement cette interface d'accès. La présente politique décrit le traitement des données personnelles dans le cadre de l'utilisation du Site, conformément au Règlement Général sur la Protection des Données (RGPD) et à la loi Informatique et Libertés du 6 janvier 1978 modifiée.</p>

  <h2>2. Absence de compte et de conservation</h2>
  <p>Le Site ne crée aucun compte utilisateur, ne demande aucune adresse email ni aucun mot de passe. Les informations saisies dans le formulaire d'accès (pseudonyme, âge déclaré, localité) ne sont pas conservées par le Site au-delà du traitement technique strictement nécessaire à l'établissement de la connexion au service de discussion tiers.</p>
  <p>Le Site n'héberge pas et ne conserve pas les conversations, messages ou contenus échangés via le service de discussion : ces traitements relèvent exclusivement du prestataire tiers exploitant ce service.</p>

  <h2>3. Données techniques</h2>
  <p>Comme tout service en ligne, des données techniques (adresse IP, date et heure de connexion, type de navigateur) sont enregistrées automatiquement dans les journaux du serveur exploité par l'éditeur, à des fins de fonctionnement, de sécurité et au titre des obligations légales de conservation. Des données techniques peuvent également être traitées au niveau de l'infrastructure d'hébergement (OVH) et par le prestataire exploitant le service de discussion, conformément à leurs politiques respectives.</p>

  <h2>4. Publicité et cookies</h2>
  <p>Le Site est susceptible de faire appel à des services tiers pouvant déposer des cookies ou traceurs sur le terminal de l'utilisateur, afin de diffuser des contenus ou  annonces (ciblées ou non) et d'en mesurer la performance.</p>
  <p>Le consentement de l'utilisateur au dépôt des traceurs soumis à consentement est recueilli, lors de la navigation, via le dispositif de gestion du consentement (CMP) affiché sur le Site. L'utilisateur peut accepter, refuser ou modifier ses choix à tout moment via ce dispositif.</p>
  <p>L'interface de connexion au service de discussion peut conserver localement, dans le navigateur de l'utilisateur (stockage local), des informations strictement fonctionnelles renseignées dans le formulaire d'accès - pseudonyme, âge, localité et genre - afin d'en faciliter la ressaisie lors d'une visite ultérieure. Ces informations demeurent stockées sur le terminal de l'utilisateur, ne sont pas transmises à des fins de suivi et ne nécessitent pas de consentement.</p>

  <h2>5. Destinataires des données</h2>
  <p>Le Site ne vend aucune donnée personnelle. Les seuls traitements de données impliquent :</p>
  <ul>
    <li>l'éditeur du Site, au titre des journaux techniques du serveur qu'il exploite ;</li>
    <li>le fournisseur d'infrastructure d'hébergement (OVH), sur les serveurs duquel le Site est exploité ;</li>
    <li>le prestataire tiers exploitant le service de discussion, pour la fourniture du service auquel l'utilisateur se connecte ;</li>
  </ul>
  <p>Des données peuvent en outre être transmises aux autorités compétentes sur réquisition, conformément à la législation en vigueur.</p>

  <h2>6. Durées de conservation</h2>
  <p>Le Site ne conservant ni compte ni contenu, il ne procède pas à une conservation prolongée de données personnelles le concernant directement. Les journaux techniques du serveur exploité par l'éditeur sont conservés pour une durée n'excédant pas un an, conformément aux obligations légales applicables. Les données techniques et de connexion traitées par l'infrastructure d'hébergement ou le prestataire tiers sont conservées selon leurs propres durées et obligations légales. Les données liées aux cookies ou traceurs déposés par des services tiers sont conservées selon les durées et modalités propres à ces services.</p>

  <h2>7. Vos droits</h2>
  <p>Conformément au RGPD, l'utilisateur dispose de droits d'accès, de rectification, d'effacement, d'opposition, de limitation et de portabilité sur ses données personnelles. Ces droits s'exercent auprès du responsable de traitement concerné :</p>
  <ul>
    <li>auprès de l'éditeur du Site, pour ce qui relève de son interface : contact@chat-caramail.fr ;</li>
    <li>auprès de l'hébergeur ou du prestataire tiers, pour les données qu'ils traitent dans le cadre de leurs services respectifs.</li>
  </ul>

  <h2>8. Modification</h2>
  <p>La présente politique peut être mise à jour pour tenir compte d'évolutions légales, techniques ou éditoriales. La version applicable est celle accessible en ligne à la date de consultation. La date de dernière mise à jour figure en haut de cette page.</p>

